def sum_and_average(num1, num2, num3):
  
  total_sum = num1 + num2 + num3
  average = total_sum/3
  return total_sum, average

num1 = 10
num2 = 40
num3 = 70

sum_of_numbers, average_of_numbers = sum_and_average(num1, num2, num3)

print("The sum is:",sum_of_numbers)
print("The average is:",average_of_numbers)
